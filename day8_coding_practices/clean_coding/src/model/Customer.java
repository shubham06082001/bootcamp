
public class Customer {
  private int id;
  private String name;
  private String address;
  private String category;
  private int price;

  Customer(int id, String name, String address, String category, int price) {
    super();
    this.id = id;
    this.name = name;
    this.address = address;
    this.category = category;
    this.price = price;
  }
}
